/**
 * @a83/orbiter-integration
 *
 * Astro integration that:
 * 1. Opens the .pod file at build/dev time
 * 2. Injects virtual module `orbiter:collections` — read published content in Astro pages
 * 3. Injects virtual module `orbiter:db` — pod path for custom server routes
 * 4. Serves media BLOBs at /orbiter/media/[id]
 * 5. Exposes a read-only JSON API at /orbiter/api/[collection]
 *
 * The admin UI runs separately via @a83/orbiter-admin.
 */
import { openPod } from '@a83/orbiter-core';
import { fileURLToPath } from 'node:url';
import { resolve, dirname } from 'node:path';
import { writeFileSync } from 'node:fs';

const __dirname = dirname(fileURLToPath(import.meta.url));

function toPascal(str) {
  return str.split(/[-_]/).map(p => p.charAt(0).toUpperCase() + p.slice(1)).join('');
}

function fieldToTs(field, allCols) {
  switch (field.type) {
    case 'string': case 'richtext': case 'url': case 'email':
    case 'date': case 'datetime': case 'image': case 'media':
      return 'string';
    case 'number':   return 'number';
    case 'boolean':  return 'boolean';
    case 'array': case 'weekdays': return 'string[]';
    case 'select':
      return field.options?.length
        ? field.options.map(o => `'${o}'`).join(' | ')
        : 'string';
    case 'relation': {
      const rel = allCols.find(c => c.id === field.collection);
      const t   = rel ? toPascal(rel.id) + 'Entry' : 'OrbiterEntry';
      return field.multiple !== false ? `${t}[]` : `${t} | null`;
    }
    default: return 'unknown';
  }
}

function generateTypes(db) {
  const cols = db.getCollections();
  const out  = [
    '// orbiter-env.d.ts — auto-generated by @a83/orbiter-integration',
    '// Regenerated on each Astro build. Do not edit manually.',
    '',
    "declare module 'orbiter:collections' {",
    '',
    '  export interface OrbiterEntry<T = Record<string, unknown>> {',
    '    id: string;',
    '    slug: string;',
    "    status: 'draft' | 'published';",
    '    created_at: string;',
    '    updated_at: string;',
    '    sort_order: number | null;',
    '    data: T;',
    '  }',
    '',
  ];

  for (const col of cols) {
    const schema = col.schema ? JSON.parse(col.schema) : {};
    const name   = toPascal(col.id);
    out.push(`  // ${col.label}`);
    out.push(`  export interface ${name}Data {`);
    for (const [key, field] of Object.entries(schema)) {
      out.push(`    ${key}?: ${fieldToTs(field, cols)};`);
    }
    out.push('  }');
    out.push(`  export type ${name}Entry = OrbiterEntry<${name}Data>;`);
    out.push('');
  }

  for (const col of cols) {
    out.push(`  export function getCollection(name: '${col.id}'): Promise<${toPascal(col.id)}Entry[]>;`);
  }
  out.push('  export function getCollection(name: string): Promise<OrbiterEntry[]>;');
  out.push('');

  for (const col of cols) {
    out.push(`  export function getEntry(collection: '${col.id}', slug: string): Promise<${toPascal(col.id)}Entry | null>;`);
  }
  out.push('  export function getEntry(collection: string, slug: string): Promise<OrbiterEntry | null>;');
  out.push('');

  out.push('  export function getLocaleCollection(name: string, locale?: string): Promise<OrbiterEntry[]>;');
  out.push('  export function getLocaleEntry(collection: string, baseSlug: string, locale: string): Promise<OrbiterEntry | null>;');
  for (const col of cols) {
    out.push(`  export function getPreviewEntry(collection: '${col.id}', slug: string, previewToken: string): Promise<${toPascal(col.id)}Entry | null>;`);
  }
  out.push('  export function getPreviewEntry(collection: string, slug: string, previewToken: string): Promise<OrbiterEntry | null>;');
  out.push('');
  out.push('  export const locale: string;');
  out.push('  export const locales: string[];');
  out.push('}');
  return out.join('\n') + '\n';
}

const VIRTUAL_COLLECTIONS_ID = 'orbiter:collections';
const RESOLVED_COLLECTIONS_ID = `\0${VIRTUAL_COLLECTIONS_ID}`;

const VIRTUAL_DB_ID = 'orbiter:db';
const RESOLVED_DB_ID = `\0${VIRTUAL_DB_ID}`;

/**
 * @param {{ pod: string }} options
 * @returns {import('astro').AstroIntegration}
 */
export default function orbiter(options = {}) {
  const podPath = options.pod ?? './content.pod';

  return {
    name: '@a83/orbiter-integration',

    hooks: {
      'astro:config:setup': ({ updateConfig, config, injectRoute, logger }) => {
        logger.info(`◆ Orbiter — loading pod: ${podPath}`);

        // Generate TypeScript types from schema
        try {
          const resolvedPod = resolve(config.root.pathname, podPath);
          const db = openPod(resolvedPod);
          const types = generateTypes(db);
          db.close();
          const outPath = resolve(config.root.pathname, 'orbiter-env.d.ts');
          writeFileSync(outPath, types, 'utf8');
          logger.info('◆ Orbiter — wrote orbiter-env.d.ts');
        } catch (e) {
          logger.warn(`◆ Orbiter — could not generate types: ${e.message}`);
        }

        const routesDir = resolve(__dirname, '../routes');

        // Media BLOB serving — public, no auth required
        injectRoute({
          pattern:    '/orbiter/media/[id]',
          entrypoint: resolve(routesDir, 'media-serve.astro'),
        });

        // Read-only JSON API
        injectRoute({
          pattern:    '/orbiter/api/[collection]',
          entrypoint: resolve(routesDir, 'api-collection.js'),
        });

        // RSS feeds per collection
        injectRoute({
          pattern:    '/orbiter/rss/[collection].xml',
          entrypoint: resolve(routesDir, 'rss-feed.js'),
        });

        // XML sitemap for all published entries
        injectRoute({
          pattern:    '/orbiter/sitemap.xml',
          entrypoint: resolve(routesDir, 'sitemap.js'),
        });

        // ── Vite virtual modules ─────────────────
        updateConfig({
          vite: {
            plugins: [
              {
                name: 'orbiter-virtual',
                resolveId(id) {
                  if (id === VIRTUAL_COLLECTIONS_ID) return RESOLVED_COLLECTIONS_ID;
                  if (id === VIRTUAL_DB_ID)          return RESOLVED_DB_ID;
                },
                load(id) {
                  const resolvedPodPath = resolve(
                    config.root.pathname,
                    podPath
                  );

                  // orbiter:collections — static snapshot of published content for build
                  if (id === RESOLVED_COLLECTIONS_ID) {
                    const db = openPod(resolvedPodPath);
                    const collections = db.getCollections();
                    const snapshot = {};
                    for (const col of collections) {
                      snapshot[col.id] = db.getEntries(col.id, { status: 'published' });
                    }

                    const defaultLocale = db.getMeta('site.locale') ?? 'en';
                    const rawLocales    = db.getMeta('site.locales') ?? defaultLocale;
                    const locales       = rawLocales.split(',').map(l => l.trim()).filter(Boolean);

                    // Resolve relation fields inline
                    const byId = {};
                    for (const entries of Object.values(snapshot)) {
                      for (const e of entries) byId[e.id] = e;
                    }
                    for (const col of collections) {
                      const colSchema = col.schema ? JSON.parse(col.schema) : {};
                      const relFields = Object.entries(colSchema).filter(([, f]) => f.type === 'relation');
                      if (!relFields.length) continue;
                      for (const entry of snapshot[col.id] ?? []) {
                        for (const [key, field] of relFields) {
                          const raw = entry.data[key];
                          const ids = Array.isArray(raw) ? raw : (raw ? [raw] : []);
                          const resolved = ids.map(id => byId[id] ?? id).filter(Boolean);
                          entry.data[key] = field.multiple !== false ? resolved : (resolved[0] ?? null);
                        }
                      }
                    }

                    db.close();

                    return `
export const collections = ${JSON.stringify(snapshot, null, 2)};

const _podPath = ${JSON.stringify(resolvedPodPath)};

/** Default locale (e.g. "de") */
export const locale = ${JSON.stringify(defaultLocale)};

/** All configured locales (e.g. ["de", "en"]) */
export const locales = ${JSON.stringify(locales)};

export function getCollection(name) {
  return Promise.resolve((collections[name] ?? []).filter(e => (e.locale ?? '') === ''));
}

export function getEntry(collection, slug) {
  const entries = collections[collection] ?? [];
  return Promise.resolve(entries.find(e => e.slug === slug && (e.locale ?? '') === '') ?? null);
}

export function getLocaleCollection(name, loc) {
  const all = collections[name] ?? [];
  // First locale in the locales array maps to locale='' in the DB
  const dbLoc = (!loc || loc === locales[0]) ? '' : loc;
  return Promise.resolve(all.filter(e => (e.locale ?? '') === dbLoc));
}

export function getLocaleEntry(collection, baseSlug, loc) {
  const entries = collections[collection] ?? [];
  const dbLoc = (!loc || loc === locales[0]) ? '' : loc;
  const variant = entries.find(e => e.slug === baseSlug && (e.locale ?? '') === dbLoc);
  if (variant) return Promise.resolve(variant);
  return Promise.resolve(entries.find(e => e.slug === baseSlug && (e.locale ?? '') === '') ?? null);
}

export async function getPreviewEntry(collection, slug, previewToken) {
  const { openPod } = await import('@a83/orbiter-core');
  const db = openPod(_podPath);
  const storedToken = db.getMeta('preview.token');
  if (!previewToken || previewToken !== storedToken) { db.close(); return null; }
  const row = db.db.prepare(
    'SELECT * FROM _entries WHERE collection_id = ? AND slug = ? AND deleted_at IS NULL'
  ).get(collection, slug);
  db.close();
  if (!row) return null;
  return { ...row, data: JSON.parse(row.data) };
}
`;
                  }

                  // orbiter:db — pod path for custom server routes
                  if (id === RESOLVED_DB_ID) {
                    return `export const podPath = ${JSON.stringify(resolvedPodPath)};`;
                  }
                },
              },
            ],
          },
        });
      },
    },
  };
}
